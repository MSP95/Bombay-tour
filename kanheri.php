          <h2><u>Kanheri Caves</u></h2><br>
          <div class="clr"></div>
         <img src="images/kanheri1.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
					<p>The Kanheri Caves (Sanskrit: कान्हेरीगुहाः Kānherī-guhāḥ) constitute a group of rock-cut monuments that are located to the north of Borivali on the western outskirts of Mumbai, India. Located within the forests of the Sanjay Gandhi National Park, the caves are 6 km from the main gate and 7 km from Borivali Station. Tourists can enter after 7:30 a.m. The Kanheri Caves demonstrate the Buddhist influence on the art and culture of India. Kanheri comes from the Sanskrit Krishnagiri, which means black mountain.[1] They were chiseled out of a massive basaltic rock outcropping
</p>

					<p><h2>Description</h2><br>These caves date from the first century BCE to the 10th century CE. One hundred and nine caves have been carved from the basalt. Unlike the elegant splendor of the adjacent Elephanta Caves, the earlier cells are spartan and unadorned. Each cave has a stone plinth for a bed. A congregation hall with huge stone pillars contains the stupa, a Buddhist shrine. Farther up the hill are canals and cisterns, the remains of an ancient system that channeled rainwater into huge tanks.[3] Once the caves were converted to permanent monasteries, the rock was carved with intricate reliefs of Buddha and the Bodhisattvas. Kanheri had become an important Buddhist settlement on the Konkan coast by the 3rd century CE.</p><p>Most of the caves are used as the Buddhist viharas, meant for living, studying, and meditating. The larger caves were chaityas, or halls for congregational worship; are lined with intricately carved Buddhist sculptures, reliefs and pillars; and contain rock-cut stupas for congregational worship. The Avalokiteshwara is the most distinctive figure. The large number of viharas demonstrates the well organized establishment of Buddhist monks. This establishment was also connected with many trade centers, such as the ports of Sopara, Kalyan, Nasik, Paithan and Ujjain. Kanheri was a University center by the time the area was under the rule of the Maurayan and Kushan empires.[5] In the late 10th century, the Buddhist teacher Atisha (980–1054) came to the Krishnagiri Vihara to study Buddhist meditation under Rahulagupta</p>
<img src="images/kanheri4.jpg" alt="image"height="500px" width="425px"><b></b></img>
