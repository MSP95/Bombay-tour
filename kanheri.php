					<p><h2>Inscriptions at Kanheri</h2><br>Nearly 51 legible inscriptions and 26 epigraphs are found at Kanheri, which include the inscriptions in Brahmi, Devanagari and 3 Pahlavi[7] epigraphs found in Cave 90.[1][8] One of the significant inscriptions mentions about the marriage of Satavahana ruler Vashishtiputra Satakarni with the daughter of Rudradaman I.</p>
