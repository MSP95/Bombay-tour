          <h2><u>Elephanta Caves</u></h2><br>
          <div class="clr"></div>
         <img src="images/elephanta1.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
					<p>The Elephanta Caves (Marathi: घारापुरीची लेणी, Gharapurichya Lenee) are a network of sculpted caves located on Elephanta Island, or Gharapuri (literally "the city of caves") in Mumbai Harbour, 10 kilometres (6.2 mi) to the east of the city of Mumbai in the Indian state of Maharashtra. The island, located on an arm of the Arabian Sea, consists of two groups of caves—the first is a large group of five Hindu caves, the second, a smaller group of two Buddhist caves. The Hindu caves contain rock cut stone sculptures, representing the Shaiva Hindu sect, dedicated to the god Shiva.

The rock cut architecture of the caves has been dated to between the 5th and 8th centuries, although the identity of the original builders is still a subject of debate. The caves are hewn from solid basalt rock. All the caves were also originally painted in the past, but now only traces remain.

The main cave (Cave 1, or the Great Cave) was a Hindu place of worship until Portuguese rule began in 1534, after which the caves suffered severe damage. This cave was renovated in the 1970s after years of neglect, and was designated a UNESCO World Heritage Site in 1987 to preserve the artwork. It is currently maintained by the Archaeological Survey of India (ASI)
</p>

					<p><h2>Geography</h2><br>Elephanta Island, or Gharapuri, is about 7 miles (11 km) east of the Apollo Bunder (Bunder in Marathi means a "pier for embarkation and disembarkation of passengers and goods") on the Mumbai Harbor and 6 miles (9.7 km) south of Pir Pal in Trombay. The island covers about 4 square miles (10 km2) at high tide and about 6 square miles (16 km2) at low tide. Gharapuri is small village on the south side of the island.[3] The Elephanta Caves can be reached by a ferry from the Gateway of India, Mumbai, which has the nearest airport and train station.The cave is closed on Monday.

The island is 1.5 miles (2.4 km) in length with two hills that rise to a height of about 500 feet (150 m). A deep ravine cuts through the heart of the island from north to south. On the west, the hill rises gently from the sea and stretches east across the ravine and rises gradually to the extreme east to a height of 568 feet (173 m). This hill is known as the Stupa hill. Forest growth with clusters of mango, tamarind, and karanj trees cover the hills with scattered palm trees. Rice fields are seen in the valley. The fore shore is made up of sand and mud with mangrove bushes on the fringe. Landing quays sit near three small hamlets known as Set Bunder in the north-west, Mora Bunder in the northeast, and Gharapuri or Raj Bunder in the south.[3]

The two hills of the island, the western and the eastern, have five rock-cut caves in the western part and a brick stupa on the eastern hill on its top composed of two caves with a few rock-cut cisterns. One of the caves on the eastern hill is unfinished. It is a protected island with a buffer zone according to a Notification issued in 1985, which also includes "a prohibited area" that stretches 1 kilometre (0.62 mi) from the shoreline.</p>

					<p><h2>Overview</h2><br>The island has two groups of caves in the rock-cut architectural style. The caves are hewn from solid basalt rock. All caves were painted in the past, but only traces remain. The larger group of caves, which consists of five caves on the western hill of the island, is well known for its Hindu sculptures. The primary cave, numbered as Cave 1, is about 1 mile (1.6 km) up a hillside, facing the ocean. It is a rock-cut temple complex that covers an area of 60,000 square feet (5,600 m2), and consists of a main chamber, two lateral chambers, courtyards, and subsidiary shrines. It is 39 metres (128 ft) deep from the front entrance to the back. The temple complex is the abode of Shiva, depicted in widely celebrated carvings which reveal his several forms and acts.[1][5][7][11]

On the eastern part of the island, on the Stupa Hill, there is a small group of caves that house Buddhist monuments. This hill is named after the religious Stupa monument that they display. One of the two caves is incomplete, while the other contains a Stupa made in brick</p>
