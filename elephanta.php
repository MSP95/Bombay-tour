          <h2><span>History </span></h2>
          
          
          <div class="clr"></div>
          <p>Since no inscriptions on any of the island have been discovered, the ancient history of the island is conjectural, at best. Pandavas, the heroes of the Hindu epic Mahabharata, and Banasura, the demon devotee of Shiva, are both credited with building temples or cut caves to live. Local tradition holds that the caves are not man-made.[3][7]

The Elephanta caves are "of unknown date and attribution". Art historians have dated the caves in the range of late 5th to late 8th century AD.[7] Archaeological excavations have unearthed a few Kshatrapa coins dated to 4th century AD. The known history is traced only to the defeat of Mauryan rulers of Konkan by the Badami Chalukyas emperor Pulakesi II (609–642) in a naval battle, in 635 AD. Elephanta was then called Puri or Purika, and served as the capital of the Konkan Mauryas. Some historians attribute the caves to the Konkan Mauryas, dating them to the mid-6th century, though others refute this claim saying a relatively small kingdom like the Konkan Mauryas could not undertake "an almost superhuman excavation effort," which was needed to carve the rock temples from solid rock and could not have the skilled labor to produce such "high quality" sculpture.[6][7]


Caves of Elephanta, c. 1905. Note the broken pillars, which were restored in the 1970s.
Some other historians attribute the construction to the Kalacuris (late 5th to 6th century), who may have had a feudal relationship with the Konkan Mauryas. In an era where polytheism was prevalent, the Elephanta main cave dedicates the monotheism of the Pashupata Shaivism sect, a sect to which Kalacuris as well as Konkan Mauryas belonged.[7]

The Chalukyas, who defeated the Kalacuris as well as the Konkan Mauryas, are also believed by some to be creators of the main cave, in the mid-7th century. The Rashtrakutas are the last claimants to the creation of the main cave, approximated to the early 7th to late 8th century. The Elephanta Shiva cave resembles in some aspects the 8th-century Rashtrakuta rock-temple Kailash at Ellora. The Trimurti of Elephanta showing the three faces of Shiva is akin to the Trinity of Brahma, Vishnu and Mahesh (Shiva), which was the royal insignia of the Rashtrakutas. The Nataraja and Ardhanarishvara sculptures are also attributed to the Rashtrakutas.[7][8]


The elephant sculpture from Elephanta is currently installed at the Jijamata Udyaan
Later, Elephanta was ruled by another Chalukyan dynasty, and then by Gujarat Sultanate, who surrendered it to the Portuguese in 1534. By then, Elephanta was called Gharapuri, which denotes a hill settlement. The name is still used in the local Marathi language. The Portuguese named the island "Elephanta Island" in honour of a huge rock-cut black stone statue of an elephant that was then installed on a mound, a short distance east of Gharapuri village. The elephant now sits in the Jijamata Udyaan zoo in Mumbai.[8][7]

Portuguese rule saw a decline in the Hindu population on the island and the abandonment of the Shiva cave (main cave) as a regular Hindu place of worship, though worship on Mahashivratri, the festival of Shiva, continued and still does.[9] The Portuguese did considerable damage to the sanctuaries. Portuguese soldiers used the reliefs of Shiva in the main cave for target practice, sparing only the Trimurti sculpture. They also removed an inscription related to the creation of the caves. While some historians solely blame the Portuguese for the destruction of the caves, others also cite water-logging and dripping rainwater as additional damaging factors. The Portuguese left in 1661 as per the marriage treaty of Charles II of England and Catherine of Braganza, daughter of King John IV of Portugal. This marriage shifted possession of the islands to the British Empire, as part of Catherine's dowry to Charles.[3][7][10]

Though the main cave was restored in the 1970s, other caves, including three consisting of important sculptures, are still badly damaged.[7] The caves were designated a UNESCO World Heritage Site in 1987 as per the cultural criteria of UNESCO: the caves "represent a masterpiece of human creative genius" and "bear a unique or at least exceptional testimony to a cultural tradition or to a civilization which is living or which has disappeared".</p>
