          <h2><u>EsselWorld</u></h2><br>
          <div class="clr"></div>
         <img src="images/essel1.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
					<p>EsselWorld is an amusement park located in Gorai, Mumbai and established in 1989.

The park is owned by Pan India Paryatan Pvt. Ltd. (PIPPL). EsselWorld along with its counterparts, Water Kingdom are stretched over 64 acres of land. Together, they are recognized as India’s Largest Amusement And Water Park as well as Asia’s Largest Theme Water Park.

EsselWorld is the largest amusement park in the country. It draws in an estimated 1.8 million visitors annually, of which approximately 300,000 are students
</p>

					<img src="images/essel2.jpg" alt="image"height="500px" width="850px"></img>

					<p><h2>Attractions</h2><br>EsselWorld is home to a variety of rides appropriate for individuals of all ages. It boasts of fourteen family rides, eleven adrenaline-pumping thrill rides and fifteen children’s rides.
Apart from the conventional features of a theme park such as rollercoasters and rides, EsselWorld is also host to Mumbai’s first ice skating rink, spanning 3400 square feet. The park maintains the rink’s temperature at a constant 4 degrees Celsius.</p>

<p>The dance floor is an added characteristic of the park that enables visitors to hold parties at the park. It enjoys colored lights, high definition audio, and a glass dance floor. To maintain it’s internatio</p>[
<p>Additionally, the theme park also comprises a 6-lane bowling alley widely known as “Riki’s Rocking Alley”. The bowling alley consists of an in-built arcade as well as a restaurant, “Tandoori One”, which serves vegetarian Indian cuisine.[8] Other culinary options across EsselWorld include Southern Treat - serving traditional South Indian delicacies, Domino’s Pizza, Taipan Chinese restaurant, Ohh! Mumbai - a taste of the local favorites, Happy Singh Da Dhaba – Punjabi cuisine, Petu’s and Mother Dairy desserts.</p>
<img src="images/essel3.jpg" alt="image"height="500px" width="850px"></img>
<p>Scattered across the park are the faces of the Fab-5, otherwise known as EsselWorld and Water Kingdom’s mascots. The Fab-5 Bandwagon is an established merchandise store that provides visitors and customers the option to purchase low-priced souvenirs.</p>

<p>In 2010, EsselWorld launched a horror ride entitled “Monsters In The Mist”, inaugurated by popular host, actor and first-ever winner of reality TV series “Roadies”, Ranvijay, and the CEO of EsselWorld Shirish Deshpande. It was titled India's Scariest Ride.</p>

<p>The ride is a 6,000 square feet indoor dark ride, with a track and car system. It runs for a total of four minutes through a dark den filled with mist and fog amidst numerous thrills and adventures. The ride comprises more than 20 scenes, which are supported by state-of-the-art animatronics and hi-end audio. The ride was designed by renowned international dark attractions manufacturer “The Attraction Factory” established in Missouri, USA.</p>
