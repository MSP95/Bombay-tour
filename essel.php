          <h2><span>Water Kingdom </span></h2>
          
          
          <div class="clr"></div>
          <p>Water Kingdom is known to be Asia's biggest water theme park and was established in 1998. Located in Gorai, Mumbai, the water park is an extension of Essel World. The water park's incredible slides and water rides are loved by all tourists. Mumbai's pride and the ultimate entertainment package for people of all age groups, Water Kingdom is built innovatively to provide tons of excitement and also offers adventure cruises as a bonus feature for tourists looking for a little more excitement. Some of the mind-blowing water rides here include the 'Brat Zone', 'Adventures Amazonia', 'Goofer's Lagoon' and 'Misphisly Hill'. Apart from the slides, rides and other fun activities, the water park also has a number of restaurants that promise the tourists lip-smacking gastronomical delights. During special days, several incredible events are organized at Water Kingdom as part of the celebrations. Water Kingdom is truly a remarkable place that offers unlimited fun for all who visit the park. Truly a magical tourist destination, this park, with its many entertaining features, attracts a number of tourists every single day
</p>
<p><h2>Main Attractions</h2><br>
Water Kingdom has several features that attract tourists from all over the world. 'Aquadrome' is a unique feature at the Water Kingdom, where a dance zone of 7000 square feet is set with numerous water sprinklers. Tourists can enjoy dancing to the music while being occasionally sprayed with water from the sprinklers. The 'Misphisly Hill' has 12 terrifying high-adrenaline rides that are extremely popular at the water park. The 'Drifting River' is a loop of gently flowing water that promises a ton of excitement with slow currents, where people can relax while they float. The 'Adventure Amazonia' is a water ride that is truly breathtaking! Some of the other enthralling attractions here include the 'Juicy Jammer', 'Turbo Terminator', 'Black Demon' and 'Lemon Drops'</p>
