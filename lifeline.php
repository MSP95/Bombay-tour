          <h2><span> </span></h2>
          
          <div class="clr"></div>
          <p>The Central Line in Mumbai bifurcates as they run into Suburban satellite towns. Central From Chhatrapati Shivaji Terminus (CST) to Kalyan it bifurcates to Kasara in the northeast and Karjat–Khopoli in southeast. These two corridors constitute the ‘Main’ Line.

The Harbour Line runs between CST and Panvel, and between CST and Andheri. The Harbour line has been further added as a link between Thane and Vashi. A new broad gauge line running from Panvel to Karjat has further extended this line.</p>

  <p>Mr. Kundan Lokhande, Motorman, Central Railway Mumbai Division, says, “Presently the local trains are capable of maximum speed of 85 km/h in regular service. The recently introduced AC/DC rakes are capable of 105 km/h during low traffic times. The actual average speed of locals on the slow lines is about 35 km/h and on the fast line about 45-50 km/h.”

Overcrowding has grown to be a compelling problem. 4700 passengers are packed into a 9-car rake during peak hours, as against the rated carrying capacity of 1700. Trains on the suburban line are on average more than 4 minutes apart, contributing to the problem of overcrowding. The impending introduction of new higher speed rakes may help address the issue.</p>

  <p>“Boarding a fast local during peak hours is an ordeal. Managing to find a floor space to place my right foot, with left foot hanging on for some time and then slowly resting on the feet of someone else. It’s just absolutely impossible to tell whose foot is trampling on ones feet in the mind boggling and suffocating rush,” says Mr. Ninad Satpute a Thane resident.
  <img src="images/t4.jpg" alt="image"height="500px" width="900px"></img>
The people, who take pride in being called as ‘Dombivlikar’, just have a knack of rushin-pushing-screaming on top of their voices while entering the local trains. It is just impossible for a person to alight at Dombivli during the peak hours. The rushing in crowd of these ‘Dombivlikars’ has become a trademark along the central line. “I don’t get the feel of boarding the local if I don’t scream and rush, even if the train is empty, “ says Ganesh K., a resident of Dombivli.</p>

  <p>Bhajans has been a characteristic of the local trains. The commuters complain that they have no time to offer prayers to the almighty since they hardly are home. So they seek opportunity during commuting to offer their prayers and also feel relaxed during the stressful journey.

These rakes are designed to seat three people per unit bench installed. However, the brotherhood of Mumbaikars has this undying spirit. This has given way to the ‘Fourth-seat’ concept, specifically in the second-class compartments. These units now accommodate four people as against the rated 3. However this fourth-seat concept did not manage to penetrate the order of the first class compartments.</p>

  <p>The railway also tried to introduce air-conditioned compartment to relieve the commuters of the ever-increasing temperatures. However hosting in air-conditioned compartment is not only expensive for the commuters to afford, but it also eats up more space. Also maintaining closed compartment where halts are sometimes less than two minutes apart is a big problem. The railways could did not make any further advances in this regard.

The trains are so much overcrowded that one cannot even find enough floor space to place his feet. People can also be seen travelling on the footboard, balanced precariously and hanging on their fingertips. With a super-Dense Crush Load of 14 to 16 standing passengers per square meter of floor space, one struggles to find a footing. People are even seen traveling on the rooftops and standing on the window bars to commute to their destinations.</p>

  <p>Yearly more than 3,500 people die on the Mumbai suburban railway track due to overcrowding during peak hours. This is believed to be the highest number of fatalities per year on any urban or suburban railway system.

  To enable the Mumbai Suburban Railway to meet the demands of the ever-growing passenger traffic, The Ministry of Railways and the Government of Maharashtra have jointly envisioned the constitution of a separate corporate entity to operate the system.</p>

  <p>“The Mumbai Railway Vikas Corporation Ltd (MRVC Ltd), a public sector unit of the Government of India under the Ministry of Railways, was incorporated under to implement the rail component of an integrated rail-cum-road urban transport project, called Mumbai Urban Transport Project (MUTP). The cost of the rail component of the project is to be shared equally by Ministry of Railways and Government of Maharashtra. Under the MUTP the city would be receiving newly designed coaches manufactured by the Integral Coach Factory, Perambur.” Says Mr. Mudgerikar.

Mr. C. Pitambaran, Senior Public Relation Officer says, “There are plans to change the DC section to 25kV Alternating Current (AC) by 2010. In preparation for this, BHEL has been retrofitting some EMUs with AC drives to allow them to operate with both DC and AC traction as the system conversion proceeds. Conversion to 25kV AC has already been done on the Titwala-Kasara section; next to be converted is Khopoli-Vangani, Vangani-Thane, and Titwala-Thane. BHEL has recently developed some AC-DC EMUs for use in the Bombay area in both the 25kV AC and 1.5kV DC traction regions. Plans have been in place to convert the entire Virar-Churchgate section to 25KV AC since a long time.”</p>
  <img src="images/t5.jpg" alt="image"height="450px" width="500px" align="center"></img>
  <p>It is a really huge operational challenge to carry out the conversion work without affecting the suburban EMU traffic, which carries millions of commuters everyday. Although, there have been some great milestones achieved, it will be atleast three to four years more till the entire section.

Whatever be the present status of the Mumbai suburban railways, the fact that millions of people have their lives dependent on it and the essence and flavour it carries along with it truly and exclusively belongs to ‘Aamchi Mumbai’.</p>

