          <h2><u>Local Trains: The Lifeline of Mumbai</u></h2>
          <div class="clr"></div>
           <img src="images/t1.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
					<p>When a true to the core Mumbaikar utters “Aamchi Mumbai”, the local trains will always be an inseparable element of the true spirits reflected in these. The locals have become a hallmark of Mumbai and one is not surprised to know that the daily commuters call the locals as their first homes; this in not surprising considering the fact that some of them spend more than six hours travelling in these trains day in and day out.

The Great Indian Peninsular Railway, which later formed the nucleus of the present day Central Railway, came into existence on April 16, 1853, when the first train on the Indian Sub-continent steamed off from Mumbai to Thane, a modest stretch of only 34 kms. Central Railway has since grown manifold.

The suburban townships of Mumbai have to shuttle to and from Mumbai day in and day out from distances well beyond 100 kms. Electric Multiple Units (EMU’s) are the lifeline of Mumbai, suburban Mumbai in particular. Given the geographical spread of the population and location of business areas, the rail network is the principal mode of mass transport in Mumbai.

The general populace commonly refers to the as local trains. The local trains ply on two zonal railways, the Western Railway (WR) and the Central Railway (CR). The Suburban system in Mumbai is one of the most complex and intensively utilised public transportation systems in the world. Spread over 303 route kilometres, it connects distant regions within the daily manageable commute.

The British built the first railway line in India in 1853. This was also the oldest railway system in Asia. The first train ran between Mumbai and Thane, a distance of 34 km. The Bombay Railway History Group has been striving to document railway heritage along this line. The Mumbai Suburban Railway, as well as Indian Railways, is an extension of this sapling planted by the British. Since then lots of radical changes have seen the developments of the suburban railway services.
  <img src="images/t2.png" alt="image"height="800px" width="900px"></img>

Mr. S.C. Mudgerikar, Chief Public Relation Officer, Central Railway, says, “The first 1500V DC EMUs used around Bombay (the first EMUs in India, 1925) were from Cammell Laird (UK) and Uerdingenwagonfabrik (Germany). Later units were supplied by Breda (Italy) as well. ICF has been building about twenty 9-car AC EMU rakes for Mumbai’s system each year.”

Owing to population explosion in Mumbai, the distant suburbs have become residential zones, but however don’t provide any employment avenues. Owing to this it is natural that the people inhabiting in these suburbs have to travel towards Mumbai for their employment purposes. The populace suburbs depend on the local trains for their daily bread and butter. Banks, mills, software firms, government offices and others services and businesses heavily rely on the railways for its sustenance. Also schools and colleges have many students and staff relying on railways for commuting. Even a single days stoppage will amount to huge losses, on an individual basis to the commuters and on a collective basic to the commercial sector. Industries like steel Plants, Cement factories, fertilizer factories, refineries, food grains, salt, fertilizers, petroleum agricultural products and agro industries are relying on railways for freight purposes.

“The suburban services runs 184 rakes of 9-car and 12-car composition are utilized to run 2067 train services. The system carries more than 6.4 million commuters on a daily basis and constitutes more than half of the total daily passenger capacity of the Indian Railways itself. It has the highest passenger density of any urban railway system in the world,” says Mr. Mudgerikar.

Western Railway, Churchgate to Virar, carries about 2.6 million passengers per day, almost 43 percent of the total Mumbai suburban rail traffic. The annual traffic density, about 255 million passenger-km per km of route, is believed to be the world record for passenger rail transport.

</p>

