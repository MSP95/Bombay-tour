          <h2><u>Global Vipassana Pagoda</u></h2><br>
          <div class="clr"></div>
         <img src="images/pagoda1.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
					<p>The Global Vipassana Pagoda is a Meditation Hall near Gorai, North-west of Mumbai, India. The Pagoda was inaugurated by Pratibha Patil, then President of India on 8 February 2009.[1] It is built on donated land on a peninsula between Gorai creek and the Arabian Sea. The pagoda is to serve as a monument of peace and harmony. The Global Vipassana Pagoda has been built out of gratitude to the Buddha, his teaching and the community of monks practicing his teaching. Its traditional Burmese design is an expression of gratitude towards the country of Myanmar for preserving the practice of Vipassana. The shape of the pagoda is a copy of the Shwedagon Pagoda in Yangon, Myanmar. It was built combining ancient Indian and modern technology to enable it to last for a thousand years.
</p>

					<p><h2>Description</h2><br>The center of the Global Vipassana Pagoda contains the world's largest stone dome built without any supporting pillars. The height of the dome is approximately 29 metres, while the height of the building is 96.12 meters, which is twice the size of the previously largest hollow stone monument in the world, the Gol Gumbaz Dome in Bijapur, India. External diameter of the largest section of the dome is 97.46m and the shorter sections is 94.82m. Internal diameter of the dome is 85.15m.[3] The inside of the pagoda is hollow and serves as a very large meditation hall with an area covering more than 6000 m2 (65,000 ft2). The massive inner dome seats over 8000 people enabling them to practice the non-sectarian Vipassana meditation as taught by Mr S.N. Goenka and now being practiced in over 100 countries. An inaugural one-day meditation course was held at the pagoda on 21 December 2008, with Mr S.N. Goenka in attendance as the teacher.</p>
<p>The aim of the pagoda complex is, among others, to express gratitude to Gautama Buddha for dispensing what followers believe is a universal teaching for the eradication of suffering, to educate the public about the life and teaching of the Buddha, and to provide a place for the practice of meditation. 10-day vipassana meditation courses are held free of charge at the meditation centre that is part of the Global Vipassana Pagoda complex.</p>
					<p><h2>Construction history</h2><br>Timeline<br>

<p>Meditators seated inside the Global Pagoda dome.
Planning for the construction of the Global Vipassana Pagoda began in 1997, while actual building work started in 2000. The pagoda consists of three sub-domes. The first and largest dome was completed in October 2006 when bone relics of Gautama Buddha were enshrined in the central locking stone of the dome on 29 October 2006, making it the world's largest structure containing relics of the Buddha. The relics were originally found in the stupa at Bhattiprolu, Guntur district, Andhra Pradesh, South India.[5] They have been donated by the Mahabodhi Society of India and the prime minister of Sri Lanka to be kept at the Global Vipassana Pagoda.[6][7] The second and third domes sit atop the first dome. Construction of the third dome was structurally completed on 21 November 2008.</p>

<p>The Global Vipassana Pagoda complex is still under construction with plans to include a museum depicting the life and teaching of Gautama Buddha that is expected to draw one hundred thousand visitors annually. The Global Vipassana Pagoda's educational displays will communicate the Buddha's universal teaching as a path towards real happiness.</p>

<p>The Global Vipassana Pagoda complex will consist of the following structures:</p>

<p>♦Pagoda dome containing relics of Buddha (complete)<br>
♦Vipassana meditation centre Dhamma Pattana (complete)<br>
♦Museum depicting life of the Buddha (Complete)<br>
♦Two smaller pagodas on the north and south side (complete)<br>
♦Library and study rooms<br>
♦Circumambulation path around the dome (complete)<br>
♦Administration building (complete)<br>
♦Underground parkade<br>
♦The south pagoda contains 108 meditation cells for use by Vipassana students taking a meditation course at the adjoining meditation centre.</p>
