          <h2><span>Construction materials</span></h2>
          
          
          <div class="clr"></div>
          <p>The foundation of the dome consists of basalt, while the dome itself is made from sandstone imported from Rajasthan. The individual blocks of sandstone weigh 600–700 kg each and are kept in place due to the unique design of the bricks. Each of the bricks interlock with the ones adjacent to it and lime mortar is used to fill in any remaining gaps. The circumambulation path is laid in marble.

The pinnacle of the pagoda is adorned with a large crystal. The spire is covered in real gold, while the rest of the pagoda is covered in gold paint. The spire is topped with a special ornamental umbrella piece donated by the Burmese. The main doors to the pagoda are wooden and hand-carved in Myanmar (Burma).</p>
