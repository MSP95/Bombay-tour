          <h2><u>Bandra–Worli Sea Link</u></h2><br>
          <div class="clr"></div>
         <img src="images/sealink1.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
					<p>The Bandra–Worli Sea Link, officially called Rajiv Gandhi Sea Link, is a cable-stayed bridge with pre-stressed concrete-steel viaducts on either side that links Bandra in the Western Suburbs of Mumbai with Worli in South Mumbai.[1] The bridge is a part of the proposed Western Freeway that will link the Western Suburbs to Nariman Point in Mumbai's main business district.

The INR16 billion (US$250 million) bridge was commissioned by the Maharashtra State Road Development Corporation (MSRDC), and built by the Hindustan Construction Company. The first four of the eight lanes of the bridge were opened to the public on 30 June 2009.[8] All eight lanes were opened on 24 March 2010.

The sea-link reduces travel time between Bandra and Worli during peak hours from 60–90 minutes to 20–30 minutes.[9] As of October 2009, BWSL had an average daily traffic of around 37,500 vehicles.
</p>

					<p><h2>Foundation and substructure</h2><br>TThe construction of the bridge's structure presented major engineering challenges. These included the highly variable geotechnical conditions due to the underlying marine geology of the seabed. At times, even for plan area of a single pile had a highly uneven foundation bed. Further compilcations included the presence of a variable intertidal zone, with parts of the foundation bed exposed in low tide and submerged in high tide.
The foundations for the BWSL's cable-stayed bridges consist of 120 reinforced concrete piles of 2,000 millimetres (6.6 ft) diameter. Those for the viaducts consist of 484 piles of 1,500 millimetres (4.9 ft). These 604 piles were driven between 6m and 34m into the substrate in geotechnical conditions that varied from highly weathered volcanic material to massive high strength rocks.
.</p>

					<p><h2>Power supply & lighting</h2><br>The bridge has a reliable and redundant power supply, backed up by diesel generators and auto mains failure panels for critical loads, such as monitoring, surveillance, emergency equipment and communication services including aviation and obstruction indicators. BWSL exclusively uses energy saving illumination systems.</p>
          <img src="images/sealink2.jpg" alt="image"height="500px" width="850px"><br><b> illuminated Bandra-Worli Sea Link Worli Skyline.</b></img>
