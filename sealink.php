          <h2><span>Surveillance and Security</span></h2>
          <div class="clr"></div>
          <p>An intelligent bridge management system (IBS) provides traffic information, surveillance, monitoring and control systems. It comprises CCTVs, automatic traffic counters and vehicle classification system, variable message signs, remote weather information system and emergency telephones. The control centre is located near the toll plaza along with the electronic tolling controls. The control system uses fibre-optic cables running the entire span of the BWSL. Toll and advanced traffic management systems were installed.
For traffic enforcement, the bridge includes facilities for vehicles to pull over when stopped by enforcement officers or in the event of a breakdown. The bridge uses mobile explosive scanners[18] for vehicles travelling on the sea link.[19][20] Scans take less than 20 seconds for each vehicle with sensors above and below the vehicles. Over 180 cars can be scanned per hour by each scanner.
The pillars and the towers supporting the bridge are protected by buoys designed to withstand explosions and collisions. These inflated buoys surround each pillar of the sea link to avoid any damage.
The BWSL is insured by New India Assurance.
The bridge tower and the control centres feature lightning protection, designed to protect the bridge monitoring, communication and power equipment from possible surges.</p>
