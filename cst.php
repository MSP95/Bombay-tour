          <h2><span>Suburban network </span></h2>
          
          
          <div class="clr"></div>
          <p>The network of suburban trains (locally known as locals, short for local trains) radiating out from this station is instrumental in keeping Mumbai running. The station serves long-distance trains as well as two of the suburban lines-the Central Line and the Harbour line. It is the westernmost terminus of Central Railway.

Central Line the trains terminate at Kurla, Ghatkopar, Thane, Dombivli, Kalyan, Ambarnath, Badlapur, Karjat, Khopoli, Asangaon, Titwala, and Kasara.
Harbour Line the trains terminate at Bandra, Andheri, Mankhurd, Vashi, Nerul, Belapur and Panvel.
CST has 18 platforms – seven are for locals trains and 12 are for long distance.</p>
