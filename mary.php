          <h2><u> MOUNT MARY BASILICA, Bandra</u></h2><br>
          <div class="clr"></div>
         <img src="images/mary11.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
					<p>For over a hundred years situated atop a hillock near Lands End Bandra, overlooking the Arabian Sea is Mount Mary’s Basilica a beautiful edifice in stone that simply stands out in its grandeur, an abode of tranquility, peace, reassurance and hope!

Mount Mary's, affectionately beckons one and all. No invitations are required to spend time with our Blessed Mother. Once inside this beautiful church even a single glance at Mary with the child Jesus is a feast for the eye, both natural and that of faith.

Thanking God or simply paying homage with the simplest or rarest of gifts, Mount Mary’s provides ample scope. For those who are in need of divine grace, to all of you who are troubled with the problems of daily life, come and worship at the Shrine of Mary. She is the Mother of all mankind. She will grant you solace,  understanding and Hope. ﻿Mother Mary leaves no one unaided.

This website would help you understand the background of this basilica, to keep yourself updated on the timings for the availability of the various services and to become aware of the other special programmes taking place in and around the basilica. You may also use this website to explore the various spiritual questions that time and again come to your mind.  
</p>
<img src="images/mary2.jpg" alt="image"height="500px" width="425px"><b></b></img>
<img src="images/mary3.jpg" alt="image"height="500px" width="425px"><b></b></img>
					<p><h2>The statue of the Virgin Mary</h2><br>Although the current church edifice is just 100 years old, the history behind the current statue of Our Lady goes back to the 16th century when Jesuit priests from Portugal brought the statue to the current location and constructed a chapel. In 1700 Arab pirates interested in the gilt-lined object held in the hand disfigured the statue by cutting off the right hand.

In 1760, the church was rebuilt and the statue was substituted with a statue of Our Lady of Navigators in St. Andrew's church nearby. This statue has an interesting legend. It goes that a Koli fisherman dreamt that he would find a statue in the sea. The statue was found floating in the sea between 1700 and 1760. A Jesuit Annual Letter dated to 1669 and published in the book St. Andrew's Church, Bandra (1616–1966) supports this claim. The Koli Fishermen call the statue as Mot Mauli, literally meaning The Pearl Mother (Mot= Moti= pearl and Mauli= Mother). However the pervious statue is now restored and now enjoys the place of honour in the bascilica. Both Hindu and Christian Kolis visit this shrine often giving it a kind of syncretic nature.

</p>

					<p><h2>The Basilica</h2><br>The basilica stands on a hillock, about 80 metres above sea level overlooking the Arabian Sea. It draws lakhs of devotees and pilgrims annually. Many of the faithful attest to the miraculous powers of the Blessed Virgin Mary. The shrine attracts people from all faiths who pray to Virgin Mary for expressing their gratitude or requesting favours. The church was destroyed in 1738 during a raid by the Marathas.

During the Bandra Fair, the entire area is decorated with festoons and buntings. Many pitch up stalls selling religious articles, roasted grams, snacks and sweets. Wax figures of the Virgin Mary, along with an assortment of candles shaped like hands, feet and various other parts of the body are sold at kiosks. The sick and the suffering choose a candle or wax figure that corresponds to their ailment and light it in Church, with the pious hope that Mother Mary will consider their appeals for help..</p>
