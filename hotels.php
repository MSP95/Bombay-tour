<h2>5.The Grand Central Mumbai</h2>
          
            <img src="images/5.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
          <p>The vibrant centre of Mumbai is now home to ITC Grand Central, Mumbai (formerly The Grand Central Mumbai). Conveniently located in the midst of the business and recreational action. The colourful patchwork of the city’s cultures – its colonial past, modern present, old English charm, and warm Indian hospitality – fill our hotel with an fabled grandeur.</p>
<p>Revel in the discerning and inviting service at the delicious restaurants. Presenting a variety of international cuisines to suit any taste, you are sure to find something to your liking. ITC Grand Central Mumbai extends a gracious welcome to guests of all ages, including the younger set who will appreciate our VIK (Very Important Kids) Program.</p>

<p>242 guest rooms and suites are infinitely appealing, with a light colour scheme and soft furnishings. Conveniently designed for international travellers, all rooms feature outlets that can accommodate both European and American electrical equipment.</p>

<p>Majestic surroundings and seemingly effortless service create a memorable experience at ITC Grand Central, Mumbai.</p>
<p>Hotel Address:<br>
Dr Babasaheb Ambedkar Road<br>
Parel<br>
Mumbai , Maharashtra 400012<br>
India</p>
