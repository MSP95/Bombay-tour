<h2>6.The Lalit Mumbai</h2>
          
            <img src="images/6.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
          <p>The LaLiT Mumbai hotel (formerly the Intercontinental The Lalit Mumbai) represents understated luxury, a peaceful oasis from the frenetic city outside. Being at the nerve centre of the fastest developing business hub, Andheri, The LaLiT Mumbai has easy access to other business districts of the financial capital of the country. The LaLiT welcomes all its guests to the best airport hotel in the city, an ideal abode for the global traveler.</p>
<p>The LaLiT Mumbai projects a magnificent architectural façade with its 368 options of hotel accommodation Mumbai, including suites that offer unparalleled elegance in a setting that is comfortable as well as luxurious. Effusing grandeur to the hilt, the presidential suites are of two types - Sher-e-Kashmir & Jaiselmer. Other well appointed suites include Spa suites, Luxury suites and Executive suites. With Asia’s largest Atrium Lobby, a lavish 7 story high feature welcoming the guests, this five star luxury hotels Mumbai is a marvelous combination of grandeur and sophistication.</p>

<p>The LaLiT Mumbai offers its guests an array of restaurants and bars to choose from, for both a private moment and a high powered official occasion. Guests have an option of choosing from Baluchi offering north west frontier to the stylish and upmarket Trendz. From the cozy and comfortable Beluga, offering a variety of imported wines and alcohol along with Oriental, Lebanese, Japanese and Continental snacks to Sutra, one of the most happening nightclub in the city. The hotel also has Box Office for the perfect movie theater experience.</p>

<p>Despite being located at one of the busiest business locality, The LaLiT Mumbai is equally apt as leisure hotels Mumbai for the leisure traveler as well. Guests can dance their worries off or even indulge in a holistic experience at the spa to unwind after a busy Mumbai day. The LaLiT Mumbai also has 5-6 conference rooms/ meeting rooms and one board room. Over 30000 square feet of banqueting facilities of The Majestic & The LaLiT Plaza makes them the largest venue in North Mumbai with a capacity for 2000 people. Two smaller halls and 5 meeting rooms are also available for banquet functions.</p>

<p>Hotel Address:<br>
The Lalit Mumbai<br>
Sahar Airport Road<br>
Mumbai , 400059<br>
India</p>
