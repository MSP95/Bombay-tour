 <h2><span>3.The Leela Mumbai</h2>
          
            <img src="images/3.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
          <p>Set amidst 11 acres of lush landscaped gardens, cascading waterfalls, lotus pools and fountains, The Leela Mumbai, only 1.5 kms from the international airport and in proximity to the North Mumbai Central Business District, is an enclave of tranquillity midst the hustle and bustle of India's commercial capital. Here, the culture, mystique and hospitality of India blends with modern facilities and services to give Mumbai's finest business hotel of the country. In a city of lively contemporary culture, this 5-star deluxe hotel combines state-of-the-art technology with tasteful decor to create a haven for the guests, welcoming both business and leisure travellers with warm graciousness.</p>
<p> Hotel Address:<br>
The Leela Mumbai<br>
Sahar Maharashtra<br>
Andheri - East<br>
Mumbai , 400053<br>
India</p>
