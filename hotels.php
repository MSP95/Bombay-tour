          <h2><span>2.The Oberoi, Mumbai</h2>
          
            <img src="images/2.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
          <p>The Oberoi, Mumbai is situated in the heart of the city’s business, financial and entertainment districts. Located on Marine Drive and minutes away from the many tourist attractions, the luxury hotel overlooks the ocean and Mumbai city’s skyline.</p>
 <p>The meticulously appointed rooms offer panoramic views of the ocean and the city, and are equipped with the latest technology. A variety of exclusive dining options cater to the refined palates of every discerning guest.</p>

 <p>Recreational facilities include an outdoor heated swimming pool, a 24-hour fitness centre and The Oberoi Spa and Salon. To provide a truly bespoke experience, the hotel offers each guest a personal butler service. Our Concierge will help you unravel everything the city has to offer, for business or leisure.</p>
 <p>Hotel Address:<br>
The Oberoi, Mumbai<br>
Nariman Point<br>
Mumbai , 400021<br>
India.</p>
