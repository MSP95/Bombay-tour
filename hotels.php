<h2><span>4.InterContinental Marine Drive Mumbai</h2>
          
            <img src="images/4.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
          <p>A jewel in the Queens Necklace the new InterContinental Marine Drive is strategically located in the heart of India's commercial capital and in close proximity to Nariman Point, the central business district. Overlooking the Arabian Sea this boutique style hotel with 59 oversized rooms and suites give you space to think, views to inspire and the latest in room entertainment to explore. All in one dream locationwe offer 24 hours Butler service, concierge and business services. A selection of unique entertainment and dining choices, a roof top swimming pool and exercise studio. Whether you are on business or leisure, this hotel will exceed your expectations!</p>
<p>Hotel Address:<br>
InterContinental Marine Drive Mumbai<br>
135 NETAJI SUBHASH ROAD<br>
Mumbai , 400020<br>
India</p>
