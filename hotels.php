          <h2><u>1.The Taj Mahal Palace </u></h2>
          <div class="clr"></div>
		  <img src="images/1.jpg" alt="image"height="500px" width="850px"></img>
					<p>Since it opened in 1903, The Taj Mahal Palace & Tower, Mumbai has created its own unique history. From Maharajas and Princes to various Kings, Presidents, CEOs and entertainers, the Taj has played the perfect host, supportive of their every need.</p>
<p>Built in 1903, the hotel is an architectural marvel and brings together Moorish, Oriental and Florentine styles. Offering panoramic views of the Arabian Sea and the Gateway of India, the hotel is a gracious landmark of the city of Mumbai, showcasing contemporary Indian influences along with beautiful vaulted alabaster ceilings, onyx columns, graceful archways, hand-woven silk carpets, crystal chandeliers, a magnificent art collection, an eclectic collection of furniture, and a dramatic cantilever stairway.</p>

<p>Over the past century, The Taj Mahal Palace & Tower, Mumbai has amassed a diverse collection of paintings and works of art and is a veritable showcase of artifacts and art of the era. From Belgian chandeliers to Goan Christian artifacts, the hotel incorporates a myriad of artistic styles and tastes. .</p>
<p>Hotel Address:<br>
The Taj Mahal Palace Mumbai<br>
1 Apollo Bunder<br>
Mumbai Maharashtra , 400 001<br>
India</p>
