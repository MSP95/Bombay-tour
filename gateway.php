          <h2><u>Gateway of India</u></h2><br>
          <div class="clr"></div>
         <img src="images/GWI1.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
					<p>The Gateway of India is one of India's most unique landmarks situated in the city of Mumbai. The colossal structure was constructed in 1924. Located at the tip of Apollo Bunder, the gateway overlooks the Mumbai harbor, bordered by the Arabian Sea in the Colaba district. The Gateway of India is a monument that marks India's chief ports and is a major tourist attraction for visitors who arrive in India for the first time. At one point of time, this monument represented the grandeur of the British Raj in India. The total construction cost of this monument was approximately 21 lakhs and the whole expense was borne by the Indian government. A favourite spot for tourists, nowadays, this monument attracts vendors, food stalls and photographers. The passing of the 'First Battalion of the Somerset Light Infantry' was recorded as the first main event that took place at the Gateway of India. This ceremony was conducted on February 28, 1948, when the last set of British troops and divisions left India, post-independence.
</p>

					<p><h2>History</h2><br>The main objective behind the construction of the Gateway of India was to commemorate the visit of King George V and Queen Mary to Bombay (Mumbai). In March 1911, Sir George Sydenham Clarke, who was then the Governor of Bombay, laid down the monument's first foundation. Although, this plan was approved only in 1914, the reclamations at Apollo Bunder were completed only in 1919. The architectural design of Gateway of India was fashioned by architect, George Wittet. It took 4 years to complete this monument's construction.</p>

					<p><h2>Architectural Layout </h2><br>The structural design of the Gateway of India is constituted of a large arch, with a height of 26m. The monument is built in yellow basalt and indissoluble concrete. The structural plan of Gateway of India is designed in the Indo-Saracenic style. One can also find traces of Muslim architectural styles incorporated in the structure of the grandiose edifice. The central dome of the monument is about 48 feet in diameter, with a total height of 83 feet. Designed with intricate latticework, the 4 turrets are the prominent features of the entire structure of the Gateway of India. There are steps constructed behind the arch of the Gateway that leads to the Arabian Sea. The monument is structured in such a way that one can witness the large expanse of the 'blue blanket' right ahead, welcoming and sending off ships and visitors.</p>
