          <h2><span>History </span></h2>
          
          
          <div class="clr"></div>
          <p>Consecrated on 19 November 1801, the original structure of the Siddhivinayak Temple was a small 3.6 m x 3.6 m square brick structure with a dome-shaped brick sikhara. The temple was built by the contractor Laxman Vithu Patil. The building was funded by a rich Agri woman named Deubai Patil. Childless, Deaubai built the temple so that the Lord should grant children to other barren women. Ramakrishna Jambhekar Maharaj, a disciple of the Hindu saint Akkalkot Swami Samarth, buried two divine idols in the front of the presiding deity of the temple on the orders on his guru. As prophesied by Swami Samarth, after 21 years after the burial of the icons, a mandar tree grew at that spot with a svayambhu Ganesha in its branches.

The 2550 sq m temple complex had two 3.6 m Deepamalas, a rest house and living quarters for the caretaker. It had an adjoining lake 30 x 40 sq. m. in size on the eastern and southern side of the temple. The lake, dug by Nardulla in the early 19th century to counter the scarcity of water, was filled up in the later years and the land is now not part of the temple complex. Around 1952, a small Hanuman shrine was built in the temple complex for the Hanuman icon that was found during the road extension project of Sayani Road near Elphinstone Road. In the 1950s and 60s, the fame of the temple spread and a significant number of devotees began visiting. However, in the same period, the owner of the plot sold some of the temple land, reducing the complex area. After 1975, the number of devotees increased dramatically.</p>
