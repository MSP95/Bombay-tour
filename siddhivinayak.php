          <h2><u>Siddhivinayak Temple</u></h2><br>
          <div class="clr"></div>
         <img src="images/siddhivinayak.jpg" alt="image"height="500px" width="850px"></img>
          <div class="clr"></div>
					<p>The Shree Siddhivinayak Ganapati Mandir is a Hindu temple dedicated to Lord Shri Ganesh. It is located in Prabhadevi, Mumbai, Maharashtra.It was originally built by Laxman Vithu and Deubai Patil on November 19, 1801. Though it is one of the richest temples in Mumbai, the current generation of Patil is staying in state of despair near the temple.

The temple has a small mandap (hall) with the shrine for Siddhi Vinayak ("Ganesha who grants your wish"). The wooden doors to the sanctum are carved with images of the Ashtavinayak (the eight manifestations of Ganesha in Maharashtra). The inner roof of the sanctum is plated with gold, and the central statue is of Ganesha. In the periphery, there is a Hanuman temple as well.
</p>

					<p><h2>Importance and status</h2><br>The Siddhivinayak Mandir evolved from a small, tiny place of worship to the Grand Temple that stands today in the later half of the twentieth century.[citation needed] Temple glory was bought not only by the politicians who frequented the temple but also Bollywood film stars who continuously visit to seek the blessings of Lord Ganesha.

Siddhivinayak is well known as “Navasacha Ganapati” or “Navasala Pavanara Ganapati” ('Ganapati bestows whenever humbly genuinely prayed a wish' in Marathi) among devotees.</p>

					<p><h2>Controversy</h2><br>The Siddhivinayak temple receives donations of around INR100 million (US$1.6 million)–INR150 million (US$2.4 million) every year, which makes it Mumbai city's richest temple trust.[4] In 2004, the Siddhivinayak Ganpati Temple Trust, which operates the temple, was accused of mismanaging donations. Consequently the Bombay High Court appointed a committee headed by retired judge V P Tipnis to scrutinize the trust's donations and probe the allegations. The committee reported that, "The most shocking aspect of the matter is that there is no method or principle followed for particular institutions. The only criteria for selection was recommendation or reference by trustees or the minister or a political heavy-weight, generally belonging to ruling party".
In 2006 the Bombay High Court directed the state government, the Siddhivinayak Temple Trust and the petitioner Keval Semlani to prepare "suggestive guidelines" for using the temple's trust funds.</p>
